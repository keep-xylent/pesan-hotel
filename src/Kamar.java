public class Kamar {
    String tipeKamar;
    String jenisKamar;
    int hargaPerMalam;

    public Kamar(String tipeKamar, String jenisKamar, int hargaPerMalam) {
        this.tipeKamar = tipeKamar;
        this.jenisKamar = jenisKamar;
        this.hargaPerMalam = hargaPerMalam;
    }
}